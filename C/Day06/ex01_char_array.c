#include <stdio.h>

int main(void)
{
    char name[] = "Hyukjin";

    printf("%s\n", name);

    return 0;
}

/*
 * Summary
 * - A string is a character array.
 * - Use char to store characters.
 * - Use %s to print a string.
 */