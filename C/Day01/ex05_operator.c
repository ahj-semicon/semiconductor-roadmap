#include <stdio.h>

int main()
{
    int num1 = 20;
    int num2 = 10;

    printf("Addition       : %d\n", num1 + num2);
    printf("Subtraction    : %d\n", num1 - num2);
    printf("Multiplication : %d\n", num1 * num2);
    printf("Division       : %d\n", num1 / num2);
    printf("Remainder      : %d\n", num1 % num2);

    return 0;
}

  /*
 * Summary
 * - + : Addition 
 * - - : Subtraction
 * - * : Multiplication
 * - / : Division 
 * - % : Remainder 
 */